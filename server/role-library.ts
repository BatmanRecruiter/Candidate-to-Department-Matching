// Must match the department names in the matcher-llm taxonomy prompt.
const TAXONOMY_DEPARTMENTS = [
  "Data Engineering",
  "Analytics",
  "Machine Learning",
  "Advisory",
  "Business Architecture",
  "Managed Services",
  "PMO",
  "Sales",
];

// Greenhouse department names that differ from the taxonomy names.
const DEPT_ALIASES: Record<string, string> = {
  "program management office (pmo)": "PMO",
  "program management office": "PMO",
  "ml & ai": "Machine Learning",
  "machine learning & ai": "Machine Learning",
};

function canonicalDepartment(raw: string): string | null {
  const trimmed = raw.trim().toLowerCase();
  const exact = TAXONOMY_DEPARTMENTS.find((d) => d.toLowerCase() === trimmed);
  if (exact) return exact;
  return DEPT_ALIASES[trimmed] ?? null;
}

// Deterministic fallback line for roles that don't have a stored LLM summary
// (e.g. the summary call failed during a sync). Built purely from structured
// fields so the digest stays byte-stable.
function fallbackSummaryLine(job: RoleLibraryJob): string {
  const parts: string[] = [];
  const opener = job.seniority ? `${job.seniority}-level role` : "Role";
  parts.push(
    job.required_yoe != null
      ? `${opener} requiring ${job.required_yoe}+ years of experience.`
      : `${opener}.`,
  );
  if (job.required_skills.length > 0) {
    parts.push(`Key skills: ${job.required_skills.slice(0, 8).join(", ")}.`);
  }
  if (job.preferred_skills.length > 0) {
    parts.push(`Preferred: ${job.preferred_skills.slice(0, 5).join(", ")}.`);
  }
  return parts.join(" ");
}

interface DigestEntry {
  title: string;
  current: boolean;
  summary: string;
}

// Pure + deterministic: fixed department order, sorted titles, no timestamps
// Hard ceiling on how much of a stored summary the digest will carry. The
// digest is paid per candidate in batch runs, so a runaway summary must not
// be able to silently inflate every request. Deterministic truncation.
const MAX_DIGEST_SUMMARY_CHARS = 220;

function capSummary(text: string): string {
  return text.length > MAX_DIGEST_SUMMARY_CHARS
    ? `${text.slice(0, MAX_DIGEST_SUMMARY_CHARS - 1)}…`
    : text;
}

// or counts. The string only changes when the library genuinely changes, so
// the cached matching system prompt is only rebuilt after a real sync change.
export function buildLibraryDigest(jobs: RoleLibraryJob[]): string {
  const taxonomyEntries = new Map<string, Map<string, DigestEntry>>();
  const otherEntries = new Map<string, Map<string, DigestEntry>>();

  for (const job of jobs) {
    if (!job.title) continue;
    const canonical = canonicalDepartment(job.department);
    const bucket = canonical
      ? taxonomyEntries
      : otherEntries;
    const deptKey = canonical ?? job.department.trim();
    if (!deptKey) continue;
    let titles = bucket.get(deptKey);
    if (!titles) {
      titles = new Map();
      bucket.set(deptKey, titles);
    }
    const current = job.is_active !== false;
    const summary = capSummary(job.summary?.trim() || fallbackSummaryLine(job));
    const existing = titles.get(job.title);
    if (!existing) {
      titles.set(job.title, { title: job.title, current, summary });
    } else {
      // A title counts as current if ANY posting with it is current, and a
      // current posting's summary wins over a historical one's.
      if (current && !existing.current) {
        existing.current = true;
        existing.summary = summary;
      }
    }
  }

  // Inactive roles can never be routed to — the model only needs to know the
  // title existed, so their summary lines are dead weight paid on every
  // candidate. Titles-only for historical entries; active entries keep their
  // (220-char-capped) summary. Stored summaries are untouched — this changes
  // rendering only, so a reactivated role gets its summary line back for free.
  const renderEntries = (titles: Map<string, DigestEntry>): string[] =>
    Array.from(titles.values())
      .sort((a, b) => (a.title < b.title ? -1 : a.title > b.title ? 1 : 0))
      .map((e) =>
        e.current
          ? `- ${e.title}: ${e.summary}`
          : `- ${e.title} (no longer posted)`,
      );

  const sections: string[] = [
    `## phData Role Library — actual roles phData has hired for (supplementary context)

Real phData job postings, grouped by department. Active roles include a summary of responsibilities and qualifications; roles marked "(no longer posted)" are historical titles that remain valid evidence of the kind of work each department does. This list is SUPPLEMENTARY — the department definitions above are authoritative, and your output must still be one of the valid department values listed below.`,
  ];

  for (const dept of TAXONOMY_DEPARTMENTS) {
    const titles = taxonomyEntries.get(dept);
    if (!titles || titles.size === 0) continue;
    sections.push(`### ${dept}\n${renderEntries(titles).join("\n")}`);
  }

  if (otherEntries.size > 0) {
    const otherLines: string[] = [
      "### Other posted departments (map candidates to the closest department above; do not output these names)",
    ];
    for (const dept of Array.from(otherEntries.keys()).sort()) {
      otherLines.push(`${dept}:`);
      otherLines.push(...renderEntries(otherEntries.get(dept)!));
    }
    sections.push(otherLines.join("\n"));
  }

  return sections.join("\n\n");
}
