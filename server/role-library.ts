// Statically import the role library + summaries so esbuild bundles them into
// the production server build. The dev server also reads the files from disk
// as a fallback so regenerating the JSON without restarting works.
import BUNDLED_LIBRARY from "../shared/role-library.json";
import BUNDLED_SUMMARIES from "../shared/role-summaries.json";

function _dirname(): string {
  try {
    // CJS at runtime
    // @ts-ignore
    if (typeof __dirname !== "undefined") return __dirname as unknown as string;
  } catch {}
  try {
    return path.dirname(fileURLToPath(import.meta.url));
  } catch {
    return process.cwd();
  }
}

function loadSharedJson<T>(fileName: string, bundled: T): T {
  const candidates = [
    path.resolve(process.cwd(), `shared/${fileName}`),
    path.resolve(_dirname(), `../shared/${fileName}`),
    path.resolve(_dirname(), `../../shared/${fileName}`),
  ];
  for (const p of candidates) {
    try {
      if (fs.existsSync(p)) {
        return JSON.parse(fs.readFileSync(p, "utf-8"));
      }
    } catch {}
  }
  return bundled;
}

// Read the role library JSON once at startup. We bundle a snapshot under
// shared/role-library.json. The build script (scripts/build-role-library.ts)
// regenerates it from the source .txt files.
function loadLibrary(): { generated_at: string; jobs: RoleLibraryJob[] } {
  const loaded = loadSharedJson<{ generated_at: string; jobs: RoleLibraryJob[] } | null>(
    "role-library.json",
    BUNDLED_LIBRARY as { generated_at: string; jobs: RoleLibraryJob[] },
  );
  if (loaded && Array.isArray(loaded.jobs)) return loaded;
  console.warn("[role-library] could not find role-library.json");
  return { generated_at: new Date().toISOString(), jobs: [] };
}

// One-time LLM summaries for bundled roles, keyed by job_id. Generated by
// scripts/backfill-role-summaries.ts and committed.
const ROLE_SUMMARIES: Record<string, string> = loadSharedJson(
  "role-summaries.json",
  BUNDLED_SUMMARIES as Record<string, string>,
);

const LIBRARY = loadLibrary();
export const AUGMENTED_LIBRARY = {
  ...LIBRARY,
  jobs: augmentRoleLibrary(LIBRARY.jobs).map((job) => ({
    ...job,
    summary: job.summary ?? ROLE_SUMMARIES[job.job_id],
  })),
};
console.log(
  `[role-library] loaded ${AUGMENTED_LIBRARY.jobs.length} jobs (generated ${AUGMENTED_LIBRARY.generated_at})`,
);

const INTERNAL_JOB_IDS = new Set(["internal-business-architect"]);

export interface CombinedLibrary {
  generated_at: string;
  jobs: RoleLibraryJob[];
  bundled_count: number;
  synced_count: number;
  synced_active_count: number;
}

// The combined library merges the bundled (historical) snapshot with EVERY
// synced role — active or not. Roles that drop off the Greenhouse feed stay
// in the library forever; isActive is carried as pure metadata (is_active).
// Deduped by job_id; bundled roles win on ID conflict so historical context
// is preserved exactly as-shipped.
let libraryCache: CombinedLibrary | null = null;
let digestCache: string | null = null;

export function invalidateRoleLibraryCache(): void {
  libraryCache = null;
  digestCache = null;
}

export async function getCombinedLibrary(): Promise<CombinedLibrary> {
  if (libraryCache) return libraryCache;
  const synced = await storage.listSyncedRoles(false);
  const activeIds = new Set(
    synced.filter((r) => r.isActive === 1).map((r) => r.jobId),
  );
  const bundledIds = new Set(
    AUGMENTED_LIBRARY.jobs.map((j) => j.job_id).filter(Boolean),
  );
  const additions: RoleLibraryJob[] = [];
  for (const row of synced) {
    if (bundledIds.has(row.jobId)) continue;
    additions.push({
      ...syncedRoleToLibraryJob(row),
      is_active: row.isActive === 1,
    });
  }
  // listSyncedRoles has no ORDER BY — sort so the library (and therefore the
  // digest and the cached matching prompt) is byte-stable between calls.
  additions.sort((a, b) => (a.job_id < b.job_id ? -1 : a.job_id > b.job_id ? 1 : 0));
  const bundled = AUGMENTED_LIBRARY.jobs.map((job) => ({
    ...job,
    is_active: INTERNAL_JOB_IDS.has(job.job_id)
      ? undefined
      : activeIds.has(job.job_id),
  }));
  libraryCache = {
    generated_at: AUGMENTED_LIBRARY.generated_at,
    jobs: [...bundled, ...additions],
    bundled_count: AUGMENTED_LIBRARY.jobs.length,
    synced_count: additions.length,
    synced_active_count: additions.filter((j) => j.is_active).length,
  };
  return libraryCache;
}

export async function getLibraryDigest(): Promise<string> {
  if (digestCache !== null) return digestCache;
  const lib = await getCombinedLibrary();
  digestCache = buildLibraryDigest(lib.jobs);
  return digestCache;
}

